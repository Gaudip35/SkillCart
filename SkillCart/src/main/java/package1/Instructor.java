package package1;

public class Instructor {
    private int id;
    private String name;

    // Default Constructor
    public Instructor() {}

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
